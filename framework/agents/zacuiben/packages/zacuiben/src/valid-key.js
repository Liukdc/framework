/**
 * 校验 Key 是否合法：必须包含至少一个中文名词
 * @param {string} key
 * @returns {boolean}
 */
export function isValidKey(key) {
  if (!key || !key.trim()) return false;

  const segments = key.match(/[\u4e00-\u9fa5]+/g) || [];

  for (const seg of segments) {
    // 先尝试匹配双字词
    for (let i = 0; i < seg.length - 1; i++) {
      if (NOUN_SET.has(seg.substring(i, i + 2))) return true;
    }
    // 再尝试单字匹配
    for (const ch of seg) {
      if (NOUN_SET.has(ch)) return true;
    }
  }

  // 无中文字符时允许纯英文 Key
  return segments.length === 0 && /[a-zA-Z]{2,}/.test(key);
}
